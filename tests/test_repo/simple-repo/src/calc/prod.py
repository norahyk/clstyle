def prod(a, b):
    """Return prod of a and b.

    関数についての説明文

    Args:
        a (int): 引数の説明
        b (int): 引数の説明.

    Returns:
        int: 戻り値の説明 (例 : True なら成功, False なら失敗.)

    Raises:
        例外の名前: 例外の説明 (例 : 引数が指定されていない場合に発生 )

    Yields:
        戻り値の型: 戻り値についての説明

    Examples:

        関数の使い方について記載

        >>> prod (1, 2)
            2

    Note:
        注意事項などを記載

    """
    return a * b